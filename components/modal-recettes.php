        <h3>Recette du hachis parmentier</h3>
        <img src="images/hachis_parmentier.jpg">
        <h4>Ingrédients</h4>
        <ul>
            <li>Pommes de terre</li>
            <li>Viande hâchée</li>
            <li>Beurre</li>
            <li>Gruyère</li>
            <li>Oignons, ail, échalotes</li>
            <li>Sel, poivre</li>
        </ul>

        <h4>Préparation</h4>
        <p> Hacher l'oignon, l'ail et l’échalote. éplucher et couper la carotte en brunoise très fine. Éplucher les pommes de terre et les couper en petits morceaux.
        Faire cuire les pommes de terre à l'eau salée ou du lait (attention à surveiller car le lait risque de déborder quand il bout) environ 30 minutes.
        Faire revenir 1 minute les carottes dans le beurre puis ajouter l'oignon, l'ail et l’échalote. Laisser encore 1 minute.
        Rajouter la viande. Saler poivrer et laisser cuire 10 minutes en remuant.
        Passer les pommes de terre au moulin à légumes ou au presse purée et y ajouter le beurre, le lait, la crème et bien saler. Mélanger rapidement.
        Disposer la viande dans un plat à gratin et recouvrir de purée. Terminer avec le fromage. Passer au four très chaud 10 minutes et 2 minutes sous le gril. </p>
